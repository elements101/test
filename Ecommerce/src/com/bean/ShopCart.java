package com.bean;

public class ShopCart {
       private SP sp;
       private int num;
       private boolean drop;

	public SP getSp() {
		return sp;
	}
	public void setSp(SP sp) {
		this.sp = sp;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
       
	public boolean isDrop() {
		return drop;
	}
	public void setDrop(boolean drop) {
		this.drop = drop;
	}
}
